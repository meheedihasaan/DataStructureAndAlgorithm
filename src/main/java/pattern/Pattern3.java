public class Pattern3 {
    
    public void printTriangle(int n) {
        for (int i = 0; i < n; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print((j+1)+" ");
            }
            System.out.print("\n");
        }
    }
    
}
